function phoneMask(value){
  const digits = String(value||"").replace(/\D/g,"").replace(/^8/,"7").slice(0,11);
  const d = digits.startsWith("7") ? digits.slice(1) : digits;
  let r = "+7"; if(d.length>0) r += ` (${d.slice(0,3)}`; if(d.length>=3) r += ")"; if(d.length>3) r += ` ${d.slice(3,6)}`; if(d.length>6) r += `-${d.slice(6,8)}`; if(d.length>8) r += `-${d.slice(8,10)}`; return r;
}

