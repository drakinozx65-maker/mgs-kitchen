async function parseApiResponse(res){
  const text = await res.text();
  let data = null;
  try { data = JSON.parse(text); } catch (e) {
    throw new Error('Сервер вернул не JSON. Проверьте, что проект открыт через PHP-сервер, а api.php доступен. Ответ: ' + text.slice(0, 300));
  }
  if(!res.ok || !data.ok) throw new Error(data.error || ('Ошибка сервера: ' + res.status));
  return data;
}
async function uploadRenderImage(file){
  if(!file) return;
  if(location.protocol === 'file:'){ alert('10 шаг работает только через сайт/PHP-сервер, не через открытие index.html файлом.'); return; }
  const ext = (file.name.split('.').pop()||'').toLowerCase();
  if(!['jpg','jpeg','png','webp'].includes(ext)){ alert('Допустимые форматы: JPG, PNG, WEBP'); return; }
  const fd = new FormData(); fd.append('file', file); fd.append('projectId', project.id || 'project');
  try{
    setSaveState('Загрузка картинки для рендера…');
    const res = await fetch(API + '?action=upload', {method:'POST', body:fd});
    const data = await parseApiResponse(res);
    project.render.imageUrl = data.url; project.render.imageName = file.name;
    project.render.prompt = buildKitchenRenderPrompt(); project.render.negativePrompt = buildKitchenNegativePrompt();
    markDirty('Загружена картинка для AI-рендера', file.name);
    renderAll(false);
  }catch(e){ alert('Не удалось загрузить картинку: ' + e.message); setSaveState('Ошибка загрузки','error'); }
}
async function generateKitchenRender(){
  if(location.protocol === 'file:'){ alert('10 шаг работает только через сайт/PHP-сервер, не через открытие index.html файлом.'); return; }
  if(!project.render.imageUrl){ alert('Сначала добавьте картинку проекта в последнем шаге.'); return; }
  project.render.prompt = project.render.prompt || buildKitchenRenderPrompt();
  project.render.negativePrompt = project.render.negativePrompt || buildKitchenNegativePrompt();
  const allowedSizes = ['1024x1024','1536x1024','1024x1536'];
  const payload = {projectId:project.id, image:project.render.imageUrl, prompt:project.render.prompt, negative_prompt:project.render.negativePrompt, style:project.render.style, quality:project.render.quality, size:allowedSizes.includes(project.render.size) ? project.render.size : '1536x1024', variations:num(project.render.variations)||1, temperature:project.render.temperature, seed:project.render.seed, control_strength:project.render.controlStrength, upscale:(project.render.flags||[]).includes("upscale") || !!project.render.upscale, kitchen_json:kitchenRenderJson()};
  const status = $('renderStatus'); if(status) status.textContent = 'Отправка в GPT API…';
  try{
    const res = await fetch(API + '?action=render', {method:'POST', headers:{'Content-Type':'application/json'}, body:JSON.stringify(payload)});
    const data = await parseApiResponse(res);
    project.render.resultUrl = data.url || data.image_url || '';
    project.render.history.unshift({date:new Date().toISOString(), mode:project.render.mode, size:payload.size, prompt:project.render.prompt, negativePrompt:project.render.negativePrompt, imageUrl:project.render.imageUrl, resultUrl:project.render.resultUrl});
    project.render.history = project.render.history.slice(0,20);
    markDirty('Сгенерирован AI-рендер кухни');
    renderAll(false);
  }catch(e){ if(status) status.textContent = 'Ошибка: ' + e.message; alert('Рендер не выполнен: ' + e.message); }
}

