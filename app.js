async function uploadRenderImage(file){
  if(!file) return;
  const ok = ['image/jpeg','image/png','image/webp'].includes(file.type);
  if(!ok){ alert('Загрузите JPG, PNG или WEBP'); return; }
  const status = document.getElementById('renderStatus');
  if(status) status.textContent = 'Загрузка изображения…';
  try{
    const fd = new FormData(); fd.append('file', file);
    const res = await fetch(API + '?action=upload', {method:'POST', body:fd});
    const text = await res.text();
    let data; try{ data = JSON.parse(text); } catch(e){ throw new Error('Сервер вернул не JSON при загрузке. Откройте проект через PHP-сервер. Ответ: '+text.slice(0,200)); }
    if(!res.ok || !data.ok) throw new Error(data.error || 'Ошибка загрузки');
    project.render = project.render || {history:[]};
    project.render.imageUrl = data.url;
    project.render.imageName = file.name;
    project.render.prompt = buildKitchenRenderPromptSafe();
    project.render.negativePrompt = buildKitchenNegativePromptSafe();
    markDirty('Загружена картинка для AI-рендера', file.name);
    renderAll(false);
  }catch(e){
    if(status) status.textContent = 'Ошибка загрузки изображения';
    alert(e.message || e);
  }
}
async function generateKitchenRender(){
  project.render = project.render || {history:[]};
  if(!Array.isArray(project.render.history)) project.render.history = [];
  if(!project.render.imageUrl){ alert('Сначала добавьте картинку проекта в последнем шаге.'); return; }
  project.render.prompt = project.render.prompt || buildKitchenRenderPromptSafe();
  project.render.negativePrompt = project.render.negativePrompt || buildKitchenNegativePromptSafe();
  const status = document.getElementById('renderStatus');
  if(status) status.textContent = 'Отправка в GPT API…';
  const allowedSizes = ['1024x1024','1536x1024','1024x1536'];
  const payload = {
    projectId: project.id,
    image: project.render.imageUrl,
    prompt: project.render.prompt,
    negative_prompt: project.render.negativePrompt,
    style: project.render.style || 'photorealistic',
    quality: project.render.quality || 'high',
    size: allowedSizes.includes(project.render.size) ? project.render.size : '1536x1024',
    variations: num(project.render.variations) || 1,
    temperature: project.render.temperature || '0.3',
    seed: project.render.seed || '',
    control_strength: project.render.controlStrength || 'high',
    upscale: Array.isArray(project.render.flags) ? project.render.flags.includes('upscale') : !!project.render.upscale,
    kitchen_json: kitchenRenderJsonSafe()
  };
  try{
    const res = await fetch(API + '?action=render', {method:'POST', headers:{'Content-Type':'application/json'}, body:JSON.stringify(payload)});
    const text = await res.text();
    let data; try{ data = JSON.parse(text); } catch(e){ throw new Error('Сервер вернул не JSON. Проверьте api.php/config.php. Ответ: '+text.slice(0,300)); }
    if(!res.ok || !data.ok) throw new Error(data.error || 'Ошибка генерации');
    project.render.resultUrl = data.url || data.image_url || '';
    project.render.history.unshift({date:new Date().toISOString(), mode:project.render.mode, size:payload.size, prompt:project.render.prompt, negativePrompt:project.render.negativePrompt, imageUrl:project.render.imageUrl, resultUrl:project.render.resultUrl});
    project.render.history = project.render.history.slice(0,20);
    markDirty('Сгенерирован AI-рендер кухни');
    renderAll(false);
  }catch(e){
    if(status) status.textContent = 'Ошибка генерации';
    alert(e.message || e);
  }
}
