function normalizeHardware(h){
  const e = {handles:[baseHandle()], hinges:[baseHinge()], drawers:[baseDrawer()], lifts:[baseLift()], pushers:[basePusher()], trays:[baseTray()], mode:""};
  h = h || {};
  const oldHinge = h.hinges && !Array.isArray(h.hinges) ? h.hinges : null;
  const oldDrawer = h.drawers && !Array.isArray(h.drawers) ? h.drawers : null;
  return {
    ...e, ...h,
    handles: Array.isArray(h.handles) && h.handles.length ? h.handles.map(x=>({...baseHandle(), ...x})) : e.handles,
    hinges: Array.isArray(h.hinges) && h.hinges.length ? h.hinges.map(x=>({...baseHinge(), ...x})) : (oldHinge ? [{...baseHinge(), ...oldHinge}] : e.hinges),
    drawers: Array.isArray(h.drawers) && h.drawers.length ? h.drawers.map(x=>({...baseDrawer(), ...x})) : (oldDrawer ? [{...baseDrawer(), ...oldDrawer}] : e.drawers),
    lifts: Array.isArray(h.lifts) && h.lifts.length ? h.lifts.map(x=>({...baseLift(), ...x})) : e.lifts,
    pushers: Array.isArray(h.pushers) && h.pushers.length && typeof h.pushers[0] === "object" ? h.pushers.map(x=>({...basePusher(), ...x})) : (Array.isArray(h.pushers) && h.pushers.length ? h.pushers.map(x=>({...basePusher(), name:String(x), type:String(x)})) : e.pushers),
    trays: Array.isArray(h.trays) && h.trays.length ? h.trays.map(x=>({...baseTray(), ...x})) : e.trays
  };
}

