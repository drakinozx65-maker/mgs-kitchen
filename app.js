async function saveProject(silent=false){
  try{
    const duplicate = findPhoneDuplicate();
    if (duplicate && !silent) {
      const ok = confirm("Найден проект с таким же телефоном: " + (duplicate.customer || duplicate.title || "без названия") + ". Все равно сохранить?");
      if (!ok) return;
    }
    if (duplicate && silent) {
      setSaveState("Есть дубль по телефону", "error");
      return;
    }
    clearTimeout(saveTimer);
    setSaveState("Сохраняю…", "saving");
    project.manager = project.manager || currentUser?.name || "";
    project.updatedAt = new Date().toISOString();
    if(!silent) addHistory("Проект сохранен вручную", `Итого: ${money(calcTotal())}`);
    saveLocal();
    const data = await api("save", {project});
    if(!data.ok) throw new Error(data.error || "неизвестная ошибка");
    project = normalizeProject(data.project);
    saveLocal();
    setSaveState("Сохранено " + new Date().toLocaleTimeString("ru-RU",{hour:"2-digit",minute:"2-digit"}), "saved");
    await loadProjects();
    renderAll(false);
  }catch(e){
    setSaveState("Ошибка сохранения", "error");
    if(!silent) alert("Ошибка сохранения: "+e.message);
  }
}

