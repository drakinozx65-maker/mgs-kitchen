function factoryFrontName(f){
  const kind = f.facadeKind || f.title || "";
  if(f.facadeKind === "ALVIC"){
    const opts = [];
    if(f.alvicJHandle) opts.push("J-ручка");
    if(f.alvicFrame) opts.push("рамочный ALVIC");
    if(f.alvicMillingLine) opts.push("фрезеровка LINE");
    if(f.alvicMillingSteel) opts.push("фрезеровка STEEL");
    if(f.alvicDiagonal) opts.push("диагональная текстура");
    return cleanFactoryLine(nonEmptyJoin(["ALVIC", f.alvicCategory, f.alvicDecorCategory, f.alvicDecor, f.thickness, opts.join(", ")], "; "));
  }
  if(f.facadeKind === "Пластик"){
    return cleanFactoryLine(nonEmptyJoin(["Пластик", f.plasticSide, f.plasticCategory, f.plasticDecor, f.thickness], "; "));
  }
  if(f.facadeKind === "Эмаль"){
    const opts = [];
    if(f.enamelMilling) opts.push("с фрезой " + (f.enamelMillingCategory||""));
    if(f.enamelTwoColors) opts.push("два цвета");
    if(f.enamelPatina) opts.push("патина");
    if(f.enamelSparkles) opts.push("блёстки");
    return cleanFactoryLine(nonEmptyJoin(["Эмаль", f.enamelThickness, f.enamelFinish, f.enamelColor, opts.join(", ")], "; "));
  }
  if(f.facadeKind === "Алюминиевая рамка / стекло"){
    return cleanFactoryLine(nonEmptyJoin(["Алюминиевая рамка", f.frameProfile, f.frameColor, f.frameInsert], "; "));
  }
  return cleanFactoryLine(nonEmptyJoin([kind, f.article, f.color, f.finish, f.thickness], "; "));
}
