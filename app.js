function calcStepWholesale(stepId){
  let total = 0;
  if(stepId === "body") project.body.materials.forEach(x=> { total += materialTotal(x); total += edgeTotal(x); });
  if(stepId === "back") total += num(project.back.qty)*num(project.back.price);
  if(stepId === "plinth") total += num(project.plinth.qty)*num(project.plinth.price);
  if(stepId === "fronts") project.fronts.items.forEach(f=>{ total += frontCalculatedBase(f); });
  if(stepId === "top"){ total += num(project.aluminum.qty)*num(project.aluminum.price); total += countertopBaseTotal(); }
  if(stepId === "hardware1") total += hardware1Wholesale();
  if(stepId === "hardware2") total += hardware2Wholesale();
  if(stepId === "hardware3") total += hardware3Wholesale();
  return total;
}

function stepMarkup(stepId){
  if(["body","back","plinth","fronts","top"].includes(stepId)) return project.markupGoods;
  if(["hardware1","hardware2","hardware3"].includes(stepId)) return project.markupServices;
  return 0;
}

